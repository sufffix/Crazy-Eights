// contains static utility methods
public static class Utility
{
    private static Random random = new Random();

    // fisher-yates shuffle algorithm
    public static void Shuffle<T>(this IList<T> list)
    {
        int n = list.Count;
        while (n > 1)
        {
            n--;
            int k = random.Next(n + 1);
            T value = list[k];
            list[k] = list[n];
            list[n] = value;
        }
    }
}